#You can use grep string filelocation to search for strings inside of a file. For example:

#grep "secret" pb.txt

#This searches for all the matches of secret inside of the text file pb.txt.

# The . is a wildcard and it means any character. So s.ing could be the words "sting" or "sling"

# The ^ is an anchor and tells grep to look at the very beginning of a line.

# The $ tells grep to seach at the end of a line.


